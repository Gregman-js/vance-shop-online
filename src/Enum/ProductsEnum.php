    public static function getProducts($category = null, $exclude = null) {
        if (null == $category) {
            return self::PRODUCTS;
        }
        return array_filter(self::PRODUCTS, function ($product) use ($category, $exclude) {
            if ($product['category']['slug'] === $category['slug']) {
                if (null === $exclude) {
                    return true;
                }
                if ($exclude['img'] === $product['img']) {
                    return false;
                }
                return true;
            }
            return false;
        });
    }
    public static function getProductBySlug($slug)
    {
        foreach (self::getProducts() as $product) {
            if (self::generateProductSlug($product) === $slug) {
                return $product;
            }
        }
        return null;
    }
    public static function generateProductSlug($product)
    {
        $text = preg_replace('~[^\pL\d]+~u', '-', $product['name']);

        $text = iconv('utf-8', 'us-ascii//TRANSLIT', $text);

        $text = preg_replace('~[^-\w]+~', '', $text);

        $text = trim($text, '-');

        $text = preg_replace('~-+~', '-', $text);

        $text = strtolower($text);

        if (empty($text)) {
            return 'n-a';
        }
        return $text;
    }
